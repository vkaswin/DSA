export const snakesAndLadders = (board: number[][]) => {
  let n = board.length;
  let maxSquares = n * n;
  let vistied = new Set<number>();

  let getRowAndCol = (num: number) => {
    let row = Math.floor((num - 1) / n);
    let col = (num - 1) % n;
    col = row % 2 == 1 ? n - 1 - col : col;
    row = n - 1 - row;
    return [row, col];
  };

  let queue: [number, number][] = [[1, 0]];

  while (queue.length) {
    let next: [number, number][] = [];

    for (let i = 0; i < queue.length; i++) {
      let [position, move] = queue[i];

      if (vistied.has(position)) continue;

      if (position === maxSquares) return move;

      vistied.add(position);
      move++;

      for (let i = 1; i <= 6; i++) {
        let newSquare = position + i;

        if (newSquare > maxSquares) continue;

        let [row, col] = getRowAndCol(newSquare);

        if (board[row][col] !== -1) newSquare = board[row][col];

        next.push([newSquare, move]);
      }
    }

    queue = next;
  }

  return -1;
};

